#Variable Data
#rules:
#1. Variable must start with a letter or underscore.
#2. Make sure variables are all lowercase.
#3. Use underscores to separate words in variable names.
#4. Avoid using reserved keywords as variable names.
#5. Use descriptive names that indicate the purpose of the variable.
#6. Avoid using single-letter variable names except for loop counters.
#7. Use plural names for collections or lists.
#8. Use singular names for individual items.
#9. Avoid using abbreviations unless they are well-known.
#10. Use consistent naming conventions throughout the codebase.
#11. Avoid using special characters in variable names.
#12. Use meaningful names that convey the variable's purpose.
#13. Use camelCase for variable names in JavaScript.
#14. Use snake_case for variable names in Python.

#valid variable names
